let name = "Abdulkadir"; // String
console.log(name);

let age = 19; // Number
console.log(typeof age);

let weight = 60.5; // Also a Number
console.log(typeof weight);

let isGraduated = false; // Boolean
console.log(typeof isGraduated);

let x; // undefined {since it is not initialized}
console.log(typeof x);
x = 10;
console.log(typeof x); // Now it is a number

let y = undefined; // set explicitly to undefined
console.log(typeof y);

let z = null; // null
console.log(typeof z); // bogus value {Object}
